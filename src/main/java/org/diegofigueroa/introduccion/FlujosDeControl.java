package org.diegofigueroa.introduccion;

public class FlujosDeControl {

    /**
     * Los flujos de control definen como se ejecutan las intrucciones de un programa
     *
     * Se divide en 3 tipos:
     *  - Secuencias
     *  - Condicional
     *  - Repetitivo
     */

    {
        // * Secuencias
        // ? Se ejecutan las instruccion en el orden que aparecen

        // * Condicional
        // ? Permite tomar decisiones basadas en condiciones
        int a = 10;
        if (a > 19){
            // Imprime el resultado
        } else {
            // Imprime otro resultado
        }

        // * Repetitivos
        // ? Son bloques de coigo que se repiten en base a una condicion
    }
}
