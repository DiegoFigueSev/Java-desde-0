package org.diegofigueroa.clasesyobjetos;

public class ClasesWrapper {

    {

        /**
         * Las clases Wrapper son clases que envuelven a los tipos de datros primitivos
         *
         * Para transformarlos en obejtos
         * Lo bueno de esto es que las clases objetos tienen metodos
         */

        int number = 10;
        // ? Cada tipo de de dato primitivo tiene su clae wrapper equivalente

        Integer integer = 3; //int
        Double myDouble = 3.0; //double
        Boolean myBool = true; //boolean
        Character myChar = 'a'; //character
        Byte myByte = 3; //byte
        Short myShort = 33; //short
        Long myLong = 32323L; //long
        Float myFloat = 3234.3f; //float
        //? Tenemos la oportunidad de usar metodos asociados

        /**
         * Estas clases Wrapper de los tipos de datos primitivo tenemos funcionalidades como:
         * - parseo
         * - toString
         * - compare
         * - max
         * - min
         */

        /**
         * El Wrapper de character nos provee de funcionalidades interesantes;
         * - isLetter
         * - isDigit
         * - isWhitespace
         * - toUpperCase
         * - toLowerCase
         */





    }

}
